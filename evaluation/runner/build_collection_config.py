"""Build scoped evaluation configs for reusable real-match collection levels."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

import yaml


def _read_yaml(path: Path) -> dict[str, Any]:
    return yaml.safe_load(path.read_text(encoding="utf-8")) or {}


def _enabled_count(path: str | Path, key: str) -> int:
    payload = _read_yaml(Path(path))
    return sum(1 for item in payload.get(key, []) if item.get("enabled", False))


def load_collection_levels(path: str | Path) -> dict[str, Any]:
    payload = _read_yaml(Path(path))
    if "collection_levels" not in payload:
        raise ValueError("missing collection_levels")
    return payload


def build_collection_config(
    levels_payload: dict[str, Any],
    *,
    level_id: str,
    run_id: str,
    launch_mode: str | None = None,
    repeats: int | None = None,
    allow_large_real: bool = False,
) -> dict[str, Any]:
    levels = levels_payload.get("collection_levels", {})
    if level_id not in levels:
        raise ValueError(f"unknown collection level: {level_id}")
    if not run_id:
        raise ValueError("run_id is required for scoped collection outputs")

    defaults = levels_payload.get("defaults", {})
    level = levels[level_id]
    resolved_repeats = int(repeats if repeats is not None else level["repeats"])
    resolved_launch_mode = launch_mode or defaults.get("launch_mode", "dry_run")
    maps_count = _enabled_count(level["maps_config"], "maps")
    opponents_count = _enabled_count(level["opponents_config"], "opponents")
    expected_match_count = maps_count * opponents_count * resolved_repeats
    large_threshold = int(defaults.get("large_real_match_threshold", 50))

    if (
        resolved_launch_mode == "real_launch"
        and expected_match_count >= large_threshold
        and not allow_large_real
    ):
        raise ValueError(
            "large real collection requires --allow-large-real "
            f"({expected_match_count} matches >= {large_threshold})"
        )

    return {
        "evaluation": {
            "name": f"phase_a_collection_{level_id}",
            "collection_level": level_id,
            "launch_mode": resolved_launch_mode,
            "bot_config": defaults.get("bot_config", "configs/bot/debug.yaml"),
            "maps_config": level["maps_config"],
            "opponents_config": level["opponents_config"],
            "repeats": resolved_repeats,
            "isolate_runs": bool(defaults.get("isolate_runs", True)),
            "run_id": run_id,
            "output_dir": level["output_dir"],
            "fail_on_crash": bool(defaults.get("fail_on_crash", False)),
            "historical": False,
            "include_historical_by_default": bool(
                defaults.get("include_historical_by_default", False)
            ),
            "expected_match_count": expected_match_count,
            "notes": [
                "generated by evaluation.runner.build_collection_config",
                "dry_run is the default and does not prove bot strength",
                "real capability claims require real_launch outputs",
            ],
        }
    }


def write_collection_config(config: dict[str, Any], output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(
        yaml.safe_dump(config, sort_keys=False, allow_unicode=True),
        encoding="utf-8",
    )


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Build a collection evaluation config.")
    parser.add_argument("--levels-config", default="configs/evaluation/phase_a_collection_levels.yaml")
    parser.add_argument("--level", required=True, choices=("smoke", "baseline", "evaluation", "regression"))
    parser.add_argument("--run-id", required=True)
    parser.add_argument("--output", required=True)
    parser.add_argument("--launch-mode", choices=("dry_run", "real_launch"), default=None)
    parser.add_argument("--repeats", type=int, default=None)
    parser.add_argument("--allow-large-real", action="store_true")
    return parser


def main() -> int:
    args = build_parser().parse_args()
    levels_payload = load_collection_levels(args.levels_config)
    config = build_collection_config(
        levels_payload,
        level_id=args.level,
        run_id=args.run_id,
        launch_mode=args.launch_mode,
        repeats=args.repeats,
        allow_large_real=args.allow_large_real,
    )
    write_collection_config(config, Path(args.output))
    evaluation = config["evaluation"]
    print(
        "collection config generated: "
        f"{args.output} level={args.level} "
        f"launch_mode={evaluation['launch_mode']} "
        f"expected_matches={evaluation['expected_match_count']} "
        f"run_id={evaluation['run_id']}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
